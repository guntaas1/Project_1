
def myAdd(num1 ,num2):   #function for Addition
    total = num1 + num2
    return total

def mySub(num1 ,num2):   #function for Subtraction
    total = num1 - num2
    return total

def myMul(num1 ,num2):   #function for Multiplication
    total = num1 * num2
    return total

def myDiv(num1 ,num2):   #function for Division
    total = (num1 / num2)
    return total

